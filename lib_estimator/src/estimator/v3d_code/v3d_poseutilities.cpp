      conv(b11, 4, b22, 4, con_11_22, 7);
      conv(b11, 4, b23, 5, con_11_23, 8);
      conv(b12, 4, b23, 5, con_12_23, 8);
      conv(b12, 4, b21, 4, con_12_21, 7);
      conv(b13, 5, b21, 4, con_13_21, 8);
