      //: b21 b22 b23 done.

      for(int i=0; i<4; i++)
         b31[i] = B[2*13+i];
      for(int i=0; i<4; i++)
         b32[i] = B[2*13+4+i];		
      for(int i=0; i<5; i++)
         b33[i] = B[2*13+8+i];
      //: b31 b32 b33 done.

