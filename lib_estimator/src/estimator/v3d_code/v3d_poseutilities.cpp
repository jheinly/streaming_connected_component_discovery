      B[1*13+12]= A[6+10*19];
	
      //: 3rd row. 13 items. 
	
      B[2*13+0] =-A[9+10*10];
      B[2*13+1] = A[8+10*10]-A[9+10*11];
      B[2*13+2] = A[8+10*11]-A[9+10*12];
