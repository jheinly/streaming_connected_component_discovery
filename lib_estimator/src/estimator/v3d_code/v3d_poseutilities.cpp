      B[0*13+11]=  A[4+10*18]-A[5+10*19];
      B[0*13+12]=  A[4+10*19];

      //: 2nd row. 13 items.
      B[1*13+0] =-A[7+10*10];
      B[1*13+1] = A[6+10*10]-A[7+10*11];
      B[1*13+2] = A[6+10*11]-A[7+10*12];
